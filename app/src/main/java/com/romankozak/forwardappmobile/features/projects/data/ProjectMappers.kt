package com.romankozak.forwardappmobile.features.projects.data

import com.romankozak.forwardappmobile.core.database.models.ProjectEntity
import com.romankozak.forwardappmobile.core.database.models.ReservedGroup
import com.romankozak.forwardappmobile.shared.data.database.models.Project

internal fun ProjectEntity.toShared(): Project =
    Project(
        id = id,
        name = name,
        description = description,
        parentId = parentId,
        createdAt = createdAt,
        updatedAt = updatedAt,
        tags = tags,
        relatedLinks = relatedLinks,
        isExpanded = isExpanded,
        order = order,
        isAttachmentsExpanded = isAttachmentsExpanded,
        defaultViewModeName = defaultViewModeName,
        isCompleted = isCompleted,
        isProjectManagementEnabled = isProjectManagementEnabled,
        projectStatus = projectStatus,
        projectStatusText = projectStatusText,
        projectLogLevel = projectLogLevel,
        totalTimeSpentMinutes = totalTimeSpentMinutes,
        valueImportance = valueImportance,
        valueImpact = valueImpact,
        effort = effort,
        cost = cost,
        risk = risk,
        weightEffort = weightEffort,
        weightCost = weightCost,
        weightRisk = weightRisk,
        rawScore = rawScore,
        displayScore = displayScore,
        scoringStatus = scoringStatus,
        showCheckboxes = showCheckboxes,
        projectType = projectType,
        reservedGroup = reservedGroup?.groupName,
    )

internal fun Project.toEntity(): ProjectEntity =
    ProjectEntity(
        id = id,
        name = name,
        description = description,
        parentId = parentId,
        createdAt = createdAt,
        updatedAt = updatedAt,
        tags = tags,
        relatedLinks = relatedLinks,
        isExpanded = isExpanded,
        order = order,
        isAttachmentsExpanded = isAttachmentsExpanded,
        defaultViewModeName = defaultViewModeName,
        isCompleted = isCompleted,
        isProjectManagementEnabled = isProjectManagementEnabled,
        projectStatus = projectStatus,
        projectStatusText = projectStatusText,
        projectLogLevel = projectLogLevel,
        totalTimeSpentMinutes = totalTimeSpentMinutes,
        valueImportance = valueImportance,
        valueImpact = valueImpact,
        effort = effort,
        cost = cost,
        risk = risk,
        weightEffort = weightEffort,
        weightCost = weightCost,
        weightRisk = weightRisk,
        rawScore = rawScore,
        displayScore = displayScore,
        scoringStatus = scoringStatus,
        showCheckboxes = showCheckboxes,
        projectType = projectType,
        reservedGroup = ReservedGroup.fromString(reservedGroup),
    )
